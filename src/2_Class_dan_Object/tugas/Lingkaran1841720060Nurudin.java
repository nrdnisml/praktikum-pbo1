package tgs_js_2;

public class Lingkaran1841720060Nurudin {
    public final double PHI=3.14;
    public double r;
    
    public double hitungLuas(double r){
        double luas = PHI * r * r;
        return luas;
    }
    
    public double hitungKeliling(double r){
        double keliling = 2 * PHI * r;
        return keliling; 
    }
}
